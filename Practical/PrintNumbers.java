package Loops;

//print numbers from 1 to 10
public class PrintNumbers
{
	public static void main(String[] args) 
	{
		//num initialized to 1
      int num = 1;
      
      //condition to check num is less than equal to 10
      while (num <= 10)
      {
          System.out.println("Number is:"+num);
          num++;
      }
  }
}