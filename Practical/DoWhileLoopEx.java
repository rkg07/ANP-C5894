package Loops;

//Simple do-while loop example
public class DoWhileLoopEx
{

	public static void main(String[] args)
	{
		//Declaring and initializing i to 1
		int i=0;
		do
		{
			//print hello world
			System.out.println("Hello World");
			i++;
		}
		//Test expression
		while(i<6);

	}
}